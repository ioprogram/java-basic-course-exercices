package it.ioprogrammatore.java.module.basics;

/*
Objective: Understand basic Java syntax, variable declaration, and initialization.
Problem: Write a Java program that declares variables of different data types (int, double, char, and String), assigns values to them, and prints them to the console.
 */

public class BasicSyntax {
    public static void main(String[] args) {
        int myInt = 25;
        double myDouble = 3.14;
        char myChar = 'A';
        String myString = "Hello, Java!";

        System.out.println("Integer: " + myInt);
        System.out.println("Double: " + myDouble);
        System.out.println("Char: " + myChar);
        System.out.println("String: " + myString);
    }
}
