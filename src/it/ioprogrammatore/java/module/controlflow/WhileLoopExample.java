package it.ioprogrammatore.java.module.controlflow;

/*
Objective: Understand and use while loops.
Problem: Write a Java program that prints the numbers from 1 to 5 using a while loop.
 */

public class WhileLoopExample {
    public static void main(String[] args) {
        int i = 1;

        while (i <= 5) {
            System.out.println(i);
            i++;
        }
    }
}
