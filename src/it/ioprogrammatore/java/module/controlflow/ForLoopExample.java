package it.ioprogrammatore.java.module.controlflow;

/*
Objective: Understand and use for loops.
Problem: Write a Java program that prints the first 10 natural numbers using a for loop.
 */

public class ForLoopExample {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
    }
}
